import java.util.*;
class SquareCube
{
public static void main(String args[])
{
int sq,cube,n;
Scanner s=new Scanner(System.in);
System.out.println("enter the number:");
n=s.nextInt();
sq=n*n;
System.out.println("square of " +n +" is :"+sq);
cube=n*n*n;
System.out.println("cube of " +n +" is:"+cube);
}
}
